function App() {
  const randomGrid = () => {
    const randomGrid = []
    for (let i = 0; i < rowNum; i++) {
      randomGrid.push(
        Array.from(Array(colNum), () => Math.round(Math.random()))
      )
    }
    return randomGrid
  }
  const [grid, setGrid] = useState(emptyGrid)
  const [toggle, setToggle] = useState(true)

  const [options, setOptions] = useState({
    speed: 10,
    size: 25,
    color: "Red",
  })

  const [run, setRun] = useState(false)
  const [gen, setGen] = useState(0)

  const runRef = useRef(run)
  runRef.current = run

  const speedRef = useRef(options.speed)
  speedRef.current = options.speed

  const simulation = () => {

    setGrid((g) => {
      return produce(g, (gridCopy) => {
        for (let i = 0; i < rowNum; i++) {
          for (let j = 0; j < colNum; j++) {
            let neighbors = 0

            possiblities.forEach(([x, y]) => {
              const newI = i + x
              const newJ = j + y
              if (newI >= 0 && newI < rowNum && newJ >= 0 && newJ < colNum) {
                neighbors += g[newI][newJ]
              }
            })

            if (neighbors < 2 || neighbors > 3) {
              gridCopy[i][j] = 0
            } else if (g[i][j] === 0 && neighbors === 3) {
              gridCopy[i][j] = 1
            }
          }
        }
      })
    })
  }

 
  const startSimulation = () => {
    if(!runRef.current) {
      setToggle(true)
      return
    }
    setGen((gen) => (gen += 1))
    simulation()
    setToggle(false)
    setTimeout(startSimulation, speedRef.current * 10)
  }

  const handleChange = (event) => {
    setOptions({
      ...options,
      [event.target.name]:
        event.target.name === "color"
          ? event.target.value
          : parseInt(event.target.value),
    })
  }
  const toggleCell = (rowI, colI) => {
    const newGrid = produce(grid, (gridCopy) => {
      gridCopy[rowI][colI] = gridCopy[rowI][colI] ? 0 : 1
    })
    setGrid(newGrid)
  }

  const generateGrid = () => {
    return grid.map((rows, i) =>
      rows.map((col, j) => (
        <div
          key={`${i} - ${j}`}
          onClick={() => (toggle ? toggleCell(i, j) : undefined)}
          style={{
            width: options.size,
            height: options.size,
            background: grid[i][j] ? options.color : "black",
            border: "solid 1px white",
          }}
        />
      ))
    )
  }

  const classes = useStyles();
  

  const onhandleChange = (event, newValue) => {
    setOptions({...options, speed: newValue});
  };
 
  return (
    <div className="container">
      <div className="rules">
            <div>
            <h1>Conway's Game of Life</h1>
            <p>A  Turing Complete Cellular Automation devised by John Horton Conway</p>
            </div>
            <div id="rules">
                <h4>Rules</h4>
                <ul>
                    <li>Any live cell with fewer than two live neighbours dies, as if by underpopulation.</li>
                    <li>Any live cell with two or three live neighbours lives on to the next generation.</li>
                    <li>Any live cell with more than three live neighbours dies, as if by overpopulation.</li>
                    <li>Any dead cell with exactly three live neighbours becomes a live cell, as if by reproduction.</li>
                    <li>Cells can only live inside the environment, once they try and escape (touch the edge); they die.</li>
                </ul>
            </div>
        </div>
        <div className = 'presets'>
          <button onClick = {()=>setGrid(pulsar)}>Pulsar</button>
          <button onClick ={()=>setGrid(gospersGliderGun)}> Gosper's Glider Gun</button>
          <button onClick ={()=>setGrid(oscillators)}>Oscillators</button>
          <form className="form">
          <input
            type="text"
            name="color"
            placeholder="Cell Color"
            onChange={handleChange}
          />
          <input
            type="text"
            name="size"
            placeholder="Cell Size"
            step="1"
            min="1"
            onChange={handleChange}
          />
         
           <div className={classes.root}>
            <Typography id="continuous-slider" gutterBottom style = {{color: 'white', fontFamily: 'consolas', fontSize: '24px'}}>
              Speed 
              <p style = {{fontSize: '16px'}}>(slide to decrease the speed)</p>
            </Typography>
            <Grid container spacing={1}>
              <Grid item>
              </Grid>
              <Grid item xs>
                <Slider value={options.speed} onChange={onhandleChange} aria-labelledby="continuous-slider" />
              </Grid>
            </Grid>
          </div>
        </form>
        </div>
      <div
        className="grid"
        style={{
          display: "grid",
          gridTemplateColumns: `repeat(${colNum}, ${options.size}px)`,
        }}
      >
        {generateGrid()}
      </div>
      <section>
     
        <div className="buttons">
        <div className = 'gen'>Generations:{gen}</div>
          <button
            onClick={() => {
              if(grid === emptyGrid) {
                return 
              }
              setRun(!run)
              if (!run) {
                runRef.current = true
                startSimulation()
              }
            }}
          >
            {run ? "Stop" : "Start"}
          </button>
          <button onClick = {() => {
            setGen((gen) => (gen += 1)) 
            simulation()}}>{"Run 1 generation"}</button>
          <button
            onClick={() => {
              setGrid(emptyGrid)
              setGen(0)
              setRun(!run)
            }}
          >
            Clear
          </button>
          <button
            onClick={() => {
              setGrid(randomGrid())
              setGen(0)
           
            }}
          >
            Random Grid
          </button>
        </div>
      </section>
     
    </div>
  )
}

export default App
